package controller;

/**
 * The ControllerInterface defines the contract for controllers in the MVC architecture of the image
 * processing application. It provides a method to start the controller's operation, which typically
 * involves initializing the application and handling user interactions.
 */
public interface ControllerInterface {

  /**
   * Starts the controller's operation. This method is responsible for initializing necessary
   * components and managing the application's flow.
   */
  void start();

}
